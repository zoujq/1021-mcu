#include <intrins.h>

//===============================================================================
#ifdef DELAY_TIME_1US_FUNCTION
/**********************************************************
* Delay_us
**********************************************************/
void  Delay_us(u8 n)
{
  u16 i;

  while(n--)
  {
    i = SYSCLK_FCPU / 1000 / 102;

    while(--i)	;
  }
}
#endif

//===============================================================================
#ifdef DELAY_TIME_10US_FUNCTION
/**********************************************************
* Delay10us
**********************************************************/
void Delay_10us(u8 n)
{
  u8 i;

  while(n--)
  {
    i = SYSCLK_FCPU / 2000000;

    while(i--)
    {
      _nop_();
      _nop_();
      _nop_();
      _nop_();
      _nop_();
      _nop_();
    }
  }
}
#endif

//===============================================================================
#ifdef DELAY_TIME_100US_FUNCTION
/**********************************************************
* Delay100us
**********************************************************/
void Delay_100us(u8 n)
{
  u8 i;

  while(n--)
  {
    for(i = 0; i < 130; i++)
    {
      _nop_();
    }
  }
}
#endif

